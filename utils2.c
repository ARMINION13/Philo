#include "./philo.h"

void	give_premium_pass(t_philo **a)
{
	int	i;
	int	j;

	j = 0;
	if ((a[0]->nop % 2) == 0)
		i = (a[0]->nop / 2);
	else
		i = ((a[0]->nop - 1) / 2);
	if (a[0]->nop == 1)
		i = 0;
	if (a[0]->nop == 0)
		i = 0;
	while (i > 0)
	{
		a[j]->first = 1;
		j += 2;
		i--;
	}
}
